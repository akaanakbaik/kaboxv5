import { useState } from 'react'
import { useTranslation } from 'react-i18next'
import { Link, useLocation } from 'react-router-dom'
import { motion, AnimatePresence } from 'framer-motion'
import { Menu, X, Home, FileText, Shield } from 'lucide-react'
import { Button } from '@/components/ui/button'
import { Sheet, SheetContent, SheetHeader, SheetTitle, SheetTrigger } from '@/components/ui/sheet'
import { cn } from '@/lib/utils'

export default function Header() {
  const { t, i18n } = useTranslation()
  const location = useLocation()
  const [isOpen, setIsOpen] = useState(false)

  const navItems = [
    { path: '/', label: t('nav.home'), icon: Home },
    { path: '/docs', label: t('nav.apiDocs'), icon: FileText },
    { path: '/terms', label: t('nav.terms'), icon: Shield },
  ]

  const isActive = (path: string) => {
    if (path === '/') return location.pathname === '/'
    return location.pathname.startsWith(path)
  }

  return (
    <header className="sticky top-0 z-50 w-full border-b bg-background/95 backdrop-blur supports-[backdrop-filter]:bg-background/60">
      <div className="w-full max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="flex h-14 items-center justify-between">
          <Link to="/" className="flex items-center space-x-2 group">
            <motion.div
              whileHover={{ scale: 1.05 }}
              whileTap={{ scale: 0.95 }}
              className="flex items-center"
            >
              <img 
                src="https://raw.githubusercontent.com/akaanakbaik/my-cdn/main/logokaboxnobg.png" 
                alt="Kabox" 
                className="h-8 w-8 object-contain"
              />
              <span className="ml-2 text-lg font-semibold tracking-tight group-hover:text-primary transition-colors">
                {t('header.title')}
              </span>
            </motion.div>
          </Link>

          <Sheet open={isOpen} onOpenChange={setIsOpen}>
            <SheetTrigger asChild>
              <Button variant="ghost" size="icon" className="h-9 w-9">
                <Menu className="h-5 w-5" />
                <span className="sr-only">{t('header.menu')}</span>
              </Button>
            </SheetTrigger>
            <SheetContent side="right" className="w-[300px] sm:w-[400px]">
              <SheetHeader>
                <SheetTitle className="flex items-center space-x-2">
                  <img 
                    src="https://raw.githubusercontent.com/akaanakbaik/my-cdn/main/logokaboxnobg.png" 
                    alt="Kabox" 
                    className="h-6 w-6 object-contain"
                  />
                  <span>{t('header.title')}</span>
                </SheetTitle>
              </SheetHeader>
              <nav className="mt-8 flex flex-col space-y-2">
                <AnimatePresence>
                  {navItems.map((item, index) => {
                    const Icon = item.icon
                    const active = isActive(item.path)
                    return (
                      <motion.div
                        key={item.path}
                        initial={{ opacity: 0, x: 20 }}
                        animate={{ opacity: 1, x: 0 }}
                        transition={{ delay: index * 0.1 }}
                      >
                        <Link
                          to={item.path}
                          onClick={() => setIsOpen(false)}
                          className={cn(
                            "flex items-center space-x-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-all duration-200",
                            active 
                              ? "bg-primary text-primary-foreground" 
                              : "hover:bg-accent hover:text-accent-foreground"
                          )}
                        >
                          <Icon className={cn("h-4 w-4", active ? "text-primary-foreground" : "text-muted-foreground")} />
                          <span>{item.label}</span>
                          {active && (
                            <motion.div
                              layoutId="activeNav"
                              className="ml-auto h-1.5 w-1.5 rounded-full bg-primary-foreground"
                            />
                          )}
                        </Link>
                      </motion.div>
                    )
                  })}
                </AnimatePresence>
              </nav>
              
              <div className="mt-auto pt-8 border-t">
                <div className="flex items-center justify-between text-xs text-muted-foreground">
                  <span>Language / Bahasa</span>
                  <div className="flex space-x-1">
                    <Button 
                      variant={i18n.language === 'id' ? 'default' : 'ghost'} 
                      size="sm" 
                      className="h-7 text-xs"
                      onClick={() => i18n.changeLanguage('id')}
                    >
                      ID
                    </Button>
                    <Button 
                      variant={i18n.language === 'en' ? 'default' : 'ghost'} 
                      size="sm" 
                      className="h-7 text-xs"
                      onClick={() => i18n.changeLanguage('en')}
                    >
                      EN
                    </Button>
                  </div>
                </div>
              </div>
            </SheetContent>
          </Sheet>
        </div>
      </div>
    </header>
  )
}
