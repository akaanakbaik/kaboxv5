import * as React from "react"
import { cn } from "@/lib/utils"

interface ProgressProps extends React.HTMLAttributes<HTMLDivElement> {
  value: number
  max?: number
  showValue?: boolean
  size?: "sm" | "md" | "lg"
  variant?: "default" | "success" | "warning" | "error"
}

const Progress = React.forwardRef<HTMLDivElement, ProgressProps>(
  ({ className, value, max = 100, showValue = true, size = "md", variant = "default", ...props }, ref) => {
    const percentage = Math.min(100, Math.max(0, (value / max) * 100))
    
    const sizeClasses = {
      sm: "h-1.5",
      md: "h-2.5",
      lg: "h-4"
    }
    
    const variantClasses = {
      default: "bg-primary",
      success: "bg-green-500",
      warning: "bg-yellow-500",
      error: "bg-red-500"
    }

    return (
      <div ref={ref} className={cn("w-full space-y-1", className)} {...props}>
        <div className={cn("w-full overflow-hidden rounded-full bg-secondary", sizeClasses[size])}>
          <div
            className={cn("h-full transition-all duration-300 ease-out rounded-full", variantClasses[variant])}
            style={{ width: `${percentage}%` }}
          />
        </div>
        {showValue && (
          <div className="flex justify-between text-xs text-muted-foreground">
            <span>{percentage.toFixed(0)}%</span>
            <span>{formatBytes(value)} / {formatBytes(max)}</span>
          </div>
        )}
      </div>
    )
  }
)
Progress.displayName = "Progress"

function formatBytes(bytes: number): string {
  if (bytes === 0) return '0 B'
  const k = 1024
  const sizes = ['B', 'KB', 'MB', 'GB']
  const i = Math.floor(Math.log(bytes) / Math.log(k))
  return parseFloat((bytes / Math.pow(k, i)).toFixed(1)) + ' ' + sizes[i]
}

export { Progress }
