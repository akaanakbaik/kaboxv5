export interface UploadFile {
  id: string
  file: File
  name: string
  size: number
  type: string
  progress: number
  status: 'pending' | 'uploading' | 'processing' | 'completed' | 'error'
  url?: string
  error?: string
}

export interface UploadResult {
  id: string
  url: string
  name: string
  size: number
  type: string
  createdAt: string
}

export interface ApiResponse<T> {
  author: string
  email: string
  success: boolean
  data?: T
  message?: string
  error?: string
}

export interface FileStatus {
  id: string
  name: string
  size: number
  status: 'uploading' | 'processing' | 'completed' | 'error'
  message: string
  chunked: boolean
  chunkCount?: number
  downloadUrl?: string
}

export interface FileInfo {
  id: string
  name: string
  size: number
  mimeType: string
  chunked: boolean
  chunkCount: number
  checksum: string
  createdAt: string
  downloadUrl: string
}

