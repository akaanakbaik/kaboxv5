import express from 'express'
import cors from 'cors'
import helmet from 'helmet'
import rateLimit from 'express-rate-limit'
import slowDown from 'express-slow-down'
import hpp from 'hpp'
import compression from 'compression'
import morgan from 'morgan'
import dotenv from 'dotenv'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

import uploadRoutes from './routes/upload.js'
import fileRoutes from './routes/files.js'
import { errorHandler } from './middleware/error.js'
import { securityHeaders } from './middleware/security.js'
import { telegramBot } from './services/telegram.js'

dotenv.config()

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)

const app = express()
const PORT = process.env.PORT || 3000

const limiter = rateLimit({
  windowMs: 1000,
  max: 10,
  message: {
    author: "aka",
    email: "akaanakbaik17@proton.me",
    success: false,
    error: "Too many requests, please slow down"
  },
  standardHeaders: true,
  legacyHeaders: false,
  handler: (req, res) => {
    telegramBot.notifyRateLimit(req.ip)
    res.status(429).json({
      author: "aka",
      email: "akaanakbaik17@proton.me",
      success: false,
      error: "Rate limit exceeded. IP blocked temporarily."
    })
  }
})

const speedLimiter = slowDown({
  windowMs: 1000,
  delayAfter: 5,
  delayMs: 500
})

app.use(helmet())
app.use(securityHeaders)
app.use(compression())
app.use(morgan('combined'))
app.use(limiter)
app.use(speedLimiter)
app.use(hpp())
app.use(cors({
  origin: process.env.VITE_APP_URL || '*',
  methods: ['GET', 'POST'],
  allowedHeaders: ['Content-Type']
}))

app.use(express.json({ limit: '10mb' }))
app.use(express.urlencoded({ extended: true, limit: '10mb' }))

app.use((req, res, next) => {
  req.requestTime = Date.now()
  telegramBot.logRequest(req)
  next()
})

app.use('/api/upload', uploadRoutes)
app.use('/api/files', fileRoutes)

app.use(express.static(join(__dirname, '../dist')))

app.get('*', (req, res) => {
  res.sendFile(join(__dirname, '../dist/index.html'))
})

app.use(errorHandler)

app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`)
  telegramBot.notifyStartup(PORT)
})

process.on('uncaughtException', (err) => {
  console.error('Uncaught Exception:', err)
  telegramBot.notifyError('Uncaught Exception', err.message)
})

process.on('unhandledRejection', (reason, promise) => {
  console.error('Unhandled Rejection at:', promise, 'reason:', reason)
  telegramBot.notifyError('Unhandled Rejection', String(reason))
})
